/**
 * Fetch comments for a specific day in a shared session
 */
export async function fetchSessionComments(
  planSessionId: string,
  dayNumber: number,
  page: number = 0
): Promise<{ comments: SessionCommentWithUser[]; hasMore: boolean }> {
  try {
    const offset = page * PAGE_SIZE;

    const { data, error, count } = await supabase
      .from('comments')
      .select('*', { count: 'exact' })
      .eq('context_type', 'plan_session')
      .eq('plan_session_id', planSessionId)
      .eq('day_number', dayNumber)
      .is('parent_comment_id', null)
      .eq('status', 'active')
      .order('created_at', { ascending: false })
      .range(offset, offset + PAGE_SIZE - 1);

    if (error) throw error;

    const comments = data || [];

    // Load user profiles
    if (comments.length > 0) {
      const userIds = [...new Set(comments.map(c => c.user_id))];
      await userProfileCache$.ensureProfiles(userIds);
    }

    const hasMore = count ? offset + PAGE_SIZE < count : false;

    return { comments: comments as SessionCommentWithUser[], hasMore };
  } catch (error) {
    console.error('[PlanService] fetchSessionComments error:', error);
    return { comments: [], hasMore: false };
  }
}

