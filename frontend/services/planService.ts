/**
 * Check and award streak rewards based on consecutive days completed
 */
export async function checkAndAwardStreakRewards(
  planSessionId: string,
  userId: string
): Promise<{
  newStreak?: number;
  streakMilestone?: { type: '7-day' | '30-day' | '365-day'; xp: number; rewardType: RewardType };
}> {
  try {
    // Fetch all completed days
    const { data, error } = await supabase
      .from('user_rewards')
      .select('day_number')
      .eq('plan_session_id', planSessionId)
      .eq('user_id', userId)
      .eq('reward_type', REWARD_TYPES.DAY_COMPLETE)
      .not('day_number', 'is', null)
      .order('day_number', { ascending: true });

    if (error || !data) return {};

    const completedDays = data.map(r => r.day_number!).sort((a, b) => a - b);

    // Calculate current streak (consecutive days ending at the most recent)
    let streak = 0;
    if (completedDays.length > 0) {
      streak = 1;
      for (let i = completedDays.length - 1; i > 0; i--) {
        if (completedDays[i] - completedDays[i - 1] === 1) {
          streak++;
        } else {
          break;
        }
      }
    }

    // Award streak rewards based on current streak
    // Track the highest milestone just reached for toast display
    let streakMilestone: { type: '7-day' | '30-day' | '365-day'; xp: number; rewardType: RewardType } | undefined;

    const streakRewards: { threshold: number; type: RewardType; milestoneType?: '7-day' | '30-day' | '365-day' }[] = [
      { threshold: 3, type: REWARD_TYPES.STREAK_3 },
      { threshold: 7, type: REWARD_TYPES.STREAK_7, milestoneType: '7-day' },
      { threshold: 14, type: REWARD_TYPES.STREAK_14 },
      { threshold: 30, type: REWARD_TYPES.STREAK_30, milestoneType: '30-day' },
      { threshold: 60, type: REWARD_TYPES.STREAK_60 },
      { threshold: 90, type: REWARD_TYPES.STREAK_90 },
      { threshold: 365, type: REWARD_TYPES.STREAK_365, milestoneType: '365-day' },
    ];

    for (const { threshold, type, milestoneType } of streakRewards) {
      if (streak >= threshold) {
        const reward = await awardReward(planSessionId, userId, type);
        // Only set milestone if this reward was just awarded (not already claimed)
        if (reward && milestoneType && streak === threshold) {
          streakMilestone = {
            type: milestoneType,
            xp: REWARD_POINTS[type],
            rewardType: type,
          };
        }
      }
    }

    // Update streak in stats (trigger handles totals, but we update streak manually)
    await supabase
      .from('session_user_stats')
      .upsert({
        plan_session_id: planSessionId,
        user_id: userId,
        current_streak: streak,
        longest_streak: streak, // Will be max'd by trigger
        updated_at: new Date().toISOString(),
      }, {
        onConflict: 'plan_session_id,user_id',
      });

    return {
      newStreak: streak,
      streakMilestone,
    };
  } catch (error) {
    console.error('[PlanService] checkAndAwardStreakRewards error:', error);
    return {};
  }
}

/**
 * Calculate plan completion bonus based on on-time day completions
 * Applies diminishing returns if multiple plans completed recently
 * @returns XP bonus amount (on-time days × PLAN_COMPLETE × multiplier)
 */
export async function calculatePlanCompleteBonus(
  planSessionId: string,
  userId: string
): Promise<{ baseBonus: number; multiplier: number; finalBonus: number }> {
  try {
    // Count only on-time day completions
    const { data, error } = await supabase
      .from('user_rewards')
      .select('day_number')
      .eq('plan_session_id', planSessionId)
      .eq('user_id', userId)
      .eq('reward_type', REWARD_TYPES.DAY_COMPLETE)
      .eq('is_on_time', true);

    if (error) {
      console.error('[PlanService] Error calculating plan bonus:', error);
      return { baseBonus: 0, multiplier: 1, finalBonus: 0 };
    }

    const onTimeDays = data?.length ?? 0;
    const baseBonus = onTimeDays * REWARD_POINTS[REWARD_TYPES.PLAN_COMPLETE];

    // Apply diminishing returns for multiple plan completions
    const plansCompletedToday = getPlansCompletedToday();
    const multiplier = getPlanCompletionMultiplier(plansCompletedToday);
    const finalBonus = Math.floor(baseBonus * multiplier);

    console.log(`[PlanService] Plan completion bonus: ${onTimeDays} on-time days × ${REWARD_POINTS[REWARD_TYPES.PLAN_COMPLETE]} × ${multiplier} = ${finalBonus} XP`);

    return { baseBonus, multiplier, finalBonus };
  } catch (error) {
    console.error('[PlanService] calculatePlanCompleteBonus error:', error);
    return { baseBonus: 0, multiplier: 1, finalBonus: 0 };
  }
}

/**
 * Award plan completion bonus (called when all days are completed)
 * Only on-time day completions count towards the bonus
 * Diminishing returns apply for multiple plan completions
 */
export async function awardPlanCompleteBonus(
  planSessionId: string,
  userId: string
): Promise<{ success: boolean; bonusPoints: number; onTimeDays: number }> {
  try {
    const { finalBonus, baseBonus } = await calculatePlanCompleteBonus(planSessionId, userId);
    const onTimeDays = REWARD_POINTS[REWARD_TYPES.PLAN_COMPLETE] > 0
      ? Math.round(baseBonus / REWARD_POINTS[REWARD_TYPES.PLAN_COMPLETE])
      : 0;

    if (finalBonus <= 0) {
      return { success: true, bonusPoints: 0, onTimeDays: 0 };
    }

    // Award the plan completion reward with calculated bonus
    const reward = await awardReward(
      planSessionId,
      userId,
      REWARD_TYPES.PLAN_COMPLETE,
      undefined,
      undefined,
      finalBonus
    );

    if (reward === null) {
      // Already awarded or error
      return { success: true, bonusPoints: 0, onTimeDays: 0 };
    }

    // Track this plan completion for diminishing returns
    trackPlanCompletion(planSessionId);

    console.log(`[PlanService] Plan complete bonus awarded: ${finalBonus} XP (${onTimeDays} on-time days)`);
    return { success: true, bonusPoints: finalBonus, onTimeDays };
  } catch (error) {
    console.error('[PlanService] awardPlanCompleteBonus error:', error);
    return { success: false, bonusPoints: 0, onTimeDays: 0 };
  }
}

