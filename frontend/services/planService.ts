/**
 * Fetch user's shared sessions with enriched data (participant counts, owner info)
 */
export async function fetchUserSharedSessionsWithDetails(userId: string): Promise<SharedSessionWithDetails[]> {
  try {
    // Get sessions where user is a participant
    const { data: participations, error: partError } = await supabase
      .from('session_participants')
      .select('plan_session_id')
      .eq('user_id', userId)
      .eq('status', 'active');

    if (partError) throw partError;

    if (!participations || participations.length === 0) return [];

    const sessionIds = participations.map(p => p.plan_session_id);

    // Fetch shared plan sessions
    const { data: sessions, error: sessionsError } = await supabase
      .from('plan_sessions')
      .select('*')
      .in('id', sessionIds)
      .eq('is_shared', true)
      .eq('status', 'active')
      .order('updated_at', { ascending: false });

    if (sessionsError) throw sessionsError;
    if (!sessions || sessions.length === 0) return [];

    // Fetch participant counts for all sessions
    const { data: participantCounts, error: countError } = await supabase
      .from('session_participants')
      .select('plan_session_id')
      .in('plan_session_id', sessionIds)
      .eq('status', 'active');

    if (countError) throw countError;

    // Count participants per session
    const countMap = new Map<string, number>();
    (participantCounts || []).forEach(p => {
      const current = countMap.get(p.plan_session_id) || 0;
      countMap.set(p.plan_session_id, current + 1);
    });

    // Fetch owner profiles
    const ownerIds = [...new Set(sessions.map(s => s.user_id))];
    await userProfileCache$.ensureProfiles(ownerIds);

    // Enrich sessions with additional data
    const enrichedSessions: SharedSessionWithDetails[] = sessions.map(session => {
      const ownerProfile = userProfileCache$.getProfile(session.user_id);
      return {
        ...session,
        participant_count: countMap.get(session.id) || 0,
        owner_display_name: ownerProfile?.display_name || undefined,
        owner_avatar_url: ownerProfile?.avatar_url,
      };
    });

    return enrichedSessions;
  } catch (error) {
    console.error('[PlanService] fetchUserSharedSessionsWithDetails error:', error);
    return [];
  }
}

/**
 * Join a shared session by invite code
 */
export async function joinSharedSession(
  inviteCode: string,
  userId: string
): Promise<{ success: boolean; sharedSession?: PlanSession; error?: string }> {
  try {
    // Find the shared session
    const session = await fetchSharedSessionByCode(inviteCode);
    if (!session) {
      return { success: false, error: 'Invalid or expired invite code' };
    }

    // Check if already a participant (including inactive records from previous leave)
    const { data: existing } = await supabase
      .from('session_participants')
      .select('id, status')
      .eq('plan_session_id', session.id)
      .eq('user_id', userId)
      .maybeSingle();

    if (existing) {
      // If already active, nothing to do
      if (existing.status === 'active') {
        return { success: true, sharedSession: session };
      }

      // If inactive (previously left), reactivate the membership
      const { error: updateError } = await supabase
        .from('session_participants')
        .update({ status: 'active' })
        .eq('id', existing.id);

      if (updateError) {
        console.error('[PlanService] Error reactivating session membership:', updateError);
        return { success: false, error: 'Failed to rejoin session' };
      }

      return { success: true, sharedSession: session };
    }

    // New participant - insert record
    const insertData: SessionParticipantInsert = {
      plan_session_id: session.id,
      user_id: userId,
      role: 'member',
      current_day: 1,
      status: 'active',
    };

    const { error } = await supabase
      .from('session_participants')
      .insert(insertData);

    if (error) {
      console.error('[PlanService] Error joining shared session:', error);
      return { success: false, error: 'Failed to join session' };
    }

    return { success: true, sharedSession: session };
  } catch (error) {
    console.error('[PlanService] joinSharedSession error:', error);
    return { success: false, error: 'Failed to join session' };
  }
}

