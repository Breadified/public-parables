/**
 * Subscribe to real-time updates for a shared session
 * Handles: comments, participants, progress updates
 * @param planSessionId - The session ID to subscribe to
 * @param dayNumber - The day number to filter comments by (prevents cross-day contamination)
 */
export function subscribeToSharedSession(planSessionId: string, dayNumber?: number): () => void {
  // Cleanup existing subscription
  if (realtimeChannel) {
    supabase.removeChannel(realtimeChannel);
    realtimeChannel = null;
  }

  // Track the subscription day context
  currentSubscriptionDay = dayNumber ?? null;

  console.log('[PlanService] Subscribing to shared session:', planSessionId, 'day:', currentSubscriptionDay);

  realtimeChannel = supabase
    .channel(`shared-session-${planSessionId}`)

    // Session comment INSERT (unified comments table with plan_session context)
    .on(
      'postgres_changes',
      {
        event: 'INSERT',
        schema: 'public',
        table: 'comments',
        filter: `plan_session_id=eq.${planSessionId}`,
      },
      async (payload) => {
        // Only process plan_session context comments
        if (payload.new.context_type !== 'plan_session') return;

        // Only add if it's for the subscription day context (prevents cross-day contamination)
        // Use tracked subscription day instead of store value to avoid race conditions
        if (currentSubscriptionDay !== null && payload.new.day_number !== currentSubscriptionDay) {
          console.log('[PlanService] Ignoring comment for different day:', payload.new.day_number, 'vs subscription day:', currentSubscriptionDay);
          return;
        }

        console.log('[PlanService] New comment:', payload);

        const { data, error } = await supabase
          .from('comments')
          .select('*')
          .eq('id', payload.new.id)
          .single();

        if (!error && data) {
          await userProfileCache$.ensureProfiles([data.user_id]);
          planStore$.addComment(data as SessionCommentWithUser);
        }
      }
    )

    // Session comment UPDATE (unified comments table)
    .on(
      'postgres_changes',
      {
        event: 'UPDATE',
        schema: 'public',
        table: 'comments',
        filter: `plan_session_id=eq.${planSessionId}`,
      },
      (payload) => {
        // Only process plan_session context comments
        if (payload.new.context_type !== 'plan_session') return;

        // Only process if it's for the subscription day context
        // Use tracked subscription day instead of store value to avoid race conditions
        if (currentSubscriptionDay !== null && payload.new.day_number !== currentSubscriptionDay) {
          return;
        }

        if (payload.new.status === 'inactive') {
          planStore$.removeComment(payload.new.id);
        } else {
          planStore$.updateComment(payload.new);
        }
      }
    )

    // Participant JOIN
    .on(
      'postgres_changes',
      {
        event: 'INSERT',
        schema: 'public',
        table: 'session_participants',
        filter: `plan_session_id=eq.${planSessionId}`,
      },
      async (payload) => {
        console.log('[PlanService] Participant joined:', payload);

        const participant = payload.new as SessionParticipant;
        await userProfileCache$.ensureProfiles([participant.user_id]);

        const profile = userProfileCache$.getProfile(participant.user_id);
        const withProfile: ParticipantWithProfile = {
          ...participant,
          displayName: profile?.display_name || undefined,
          avatarUrl: profile?.avatar_url,
        };

        planStore$.addParticipant(withProfile);
      }
    )

    // Participant UPDATE (progress change, status change)
    .on(
      'postgres_changes',
      {
        event: 'UPDATE',
        schema: 'public',
        table: 'session_participants',
        filter: `plan_session_id=eq.${planSessionId}`,
      },
      (payload) => {
        console.log('[PlanService] Participant updated:', payload);

        if (payload.new.status === 'inactive') {
          planStore$.removeParticipant(payload.new.id);
        } else {
          // Real-time progress sync
          planStore$.updateParticipantProgress(
            payload.new.user_id,
            payload.new.current_day
          );
        }
      }
    )

    // Comment like INSERT (unified comment_likes table)
    .on(
      'postgres_changes',
      {
        event: 'INSERT',
        schema: 'public',
        table: 'comment_likes',
      },
      (payload) => {
        const likeData = payload.new as { comment_id: string; user_id: string };
        if (likeData.user_id !== currentUserId) {
          planStore$.updateCommentLikeCount(likeData.comment_id, 1);
        }
      }
    )

    // Comment like DELETE (unified comment_likes table)
    .on(
      'postgres_changes',
      {
        event: 'DELETE',
        schema: 'public',
        table: 'comment_likes',
      },
      (payload) => {
        const likeData = payload.old as { comment_id: string; user_id: string };
        if (likeData.user_id !== currentUserId) {
          planStore$.updateCommentLikeCount(likeData.comment_id, -1);
        }
      }
    )

    .subscribe((status) => {
      console.log('[PlanService] Subscription status:', status);
    });

  return () => {
    if (realtimeChannel) {
      console.log('[PlanService] Unsubscribing from shared session');
      supabase.removeChannel(realtimeChannel);
      realtimeChannel = null;
    }
  };
}

/**
 * Initialize shared session - load participants, comments, set up real-time
 * Uses cache-first strategy for offline support
 */
export async function initializeSharedSession(
  planSessionId: string,
  userId: string,
  dayNumber: number = 1
): Promise<void> {
  console.log('[PlanService] Initializing shared session:', planSessionId);

  setCurrentUserId(userId);
  planStore$.setActiveSharedSession(planSessionId);
  planStore$.setSelectedDay(dayNumber);
  planStore$.participantsLoading.set(true);
  planStore$.commentsLoading.set(true);

  try {
    // CACHE-FIRST: Load cached comments immediately
    const cached = await planStore$.loadSessionCommentsFromStorage(planSessionId, dayNumber);
    if (cached?.comments?.length > 0) {
      console.log('[PlanService] Loading cached comments first:', cached.comments.length);
      // Set comments for this specific day
      const currentCommentsByDay = planStore$.commentsByDay.peek();
      planStore$.commentsByDay.set({ ...currentCommentsByDay, [dayNumber]: cached.comments });
      planStore$.userLikedCommentIds.set(cached.likedIds || []);
      planStore$.commentsInitialized.set(true);
      // User sees cached comments immediately
    }

    // Check network status
    const netState = await NetInfo.fetch();
    const isOnline = netState.isConnected ?? false;

    if (!isOnline) {
      // Offline: Stay with cached data
      console.log('[PlanService] Offline - using cached session data only');
      planStore$.participantsLoading.set(false);
      planStore$.commentsLoading.set(false);
      planStore$.commentsInitialized.set(true);
      return;
    }

    // Online: Load fresh data (background refresh if cache was shown)
    const [participants, commentsResult, likedIds] = await Promise.all([
      fetchSessionParticipants(planSessionId),
      fetchSessionComments(planSessionId, dayNumber, 0),
      fetchUserSessionLikes(userId, planSessionId),
    ]);

    planStore$.setParticipants(participants);
    // Set comments for this specific day
    const currentCommentsByDay = planStore$.commentsByDay.peek();
    planStore$.commentsByDay.set({ ...currentCommentsByDay, [dayNumber]: commentsResult.comments });
    planStore$.commentsHasMore.set(commentsResult.hasMore);
    planStore$.userLikedCommentIds.set(likedIds);

    // Subscribe to real-time updates (only when online)
    // Pass dayNumber to track subscription context
    subscribeToSharedSession(planSessionId, dayNumber);

    // Cache for offline
    await planStore$.saveSessionCommentsToStorage(planSessionId, dayNumber);
  } catch (error) {
    console.error('[PlanService] initializeSharedSession error:', error);
    // On error, keep any cached data that was loaded
  } finally {
    planStore$.participantsLoading.set(false);
    planStore$.commentsLoading.set(false);
    planStore$.commentsInitialized.set(true);
  }
}

/**
 * Load comments for a different day in the same session
 * Uses cache-first strategy for offline support
 *
 * Comments are stored by day number in commentsByDay, so each day's comments
 * are isolated and won't interfere with other days.
 */
export async function loadCommentsForDay(
  planSessionId: string,
  dayNumber: number,
  userId: string
): Promise<void> {
  console.log('[PlanService] loadCommentsForDay:', dayNumber);

  planStore$.commentsLoading.set(true);
  planStore$.commentsInitialized.set(false);

  try {
    // CACHE-FIRST: Load cached comments for this day immediately
    const cached = await planStore$.loadSessionCommentsFromStorage(planSessionId, dayNumber);

    if (cached?.comments?.length > 0) {
      console.log('[PlanService] Loading cached comments for day', dayNumber, ':', cached.comments.length);
      // Set comments for this specific day
      const currentCommentsByDay = planStore$.commentsByDay.peek();
      planStore$.commentsByDay.set({ ...currentCommentsByDay, [dayNumber]: cached.comments });
      planStore$.userLikedCommentIds.set(cached.likedIds || []);
      planStore$.commentsInitialized.set(true);
    }

    // Check network status
    const netState = await NetInfo.fetch();
    const isOnline = netState.isConnected ?? false;

    if (!isOnline) {
      // Offline: Stay with cached data
      console.log('[PlanService] Offline - using cached comments only');
      planStore$.commentsLoading.set(false);
      planStore$.commentsInitialized.set(true);
      return;
    }

    // Online: Load fresh data
    const [commentsResult, likedIds] = await Promise.all([
      fetchSessionComments(planSessionId, dayNumber, 0),
      fetchUserSessionLikes(userId, planSessionId),
    ]);

    // Set comments for this specific day (won't affect other days)
    const currentCommentsByDay = planStore$.commentsByDay.peek();
    planStore$.commentsByDay.set({ ...currentCommentsByDay, [dayNumber]: commentsResult.comments });
    planStore$.commentsHasMore.set(commentsResult.hasMore);
    planStore$.commentsPage.set(0);
    planStore$.userLikedCommentIds.set(likedIds);

    // Cache for offline
    await planStore$.saveSessionCommentsToStorage(planSessionId, dayNumber);
  } catch (error) {
    console.error('[PlanService] loadCommentsForDay error:', error);
    // On error, keep any cached data
  } finally {
    planStore$.commentsLoading.set(false);
    planStore$.commentsInitialized.set(true);
  }
}

/**
 * Load more comments (pagination)
 * Only works when online
 */
export async function loadMoreSessionComments(
  planSessionId: string,
  dayNumber: number
): Promise<void> {
  const currentPage = planStore$.commentsPage.get();
  const hasMore = planStore$.commentsHasMore.get();

  if (!hasMore) return;

  // Check network before fetching more
  const netState = await NetInfo.fetch();
  if (!netState.isConnected) {
    console.log('[PlanService] Offline - cannot load more comments');
    return;
  }

  try {
    const nextPage = currentPage + 1;
    const { comments, hasMore: moreAvailable } = await fetchSessionComments(
      planSessionId,
      dayNumber,
      nextPage
    );

    // Append to the specific day's comments
    const currentCommentsByDay = planStore$.commentsByDay.peek();
    const existingComments = currentCommentsByDay[dayNumber] || [];
    planStore$.commentsByDay.set({
      ...currentCommentsByDay,
      [dayNumber]: [...existingComments, ...comments],
    });
    planStore$.commentsPage.set(nextPage);
    planStore$.commentsHasMore.set(moreAvailable);
  } catch (error) {
    console.error('[PlanService] loadMoreSessionComments error:', error);
  }
}

