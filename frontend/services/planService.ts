/**
 * Fetch rewards summary per day (for displaying trophies in session view)
 */
export async function fetchDayRewardsSummary(
  planSessionId: string,
  userId: string
): Promise<DayRewardsSummary[]> {
  try {
    const { data, error } = await supabase
      .from('user_rewards')
      .select('day_number, reward_type')
      .eq('plan_session_id', planSessionId)
      .eq('user_id', userId)
      .not('day_number', 'is', null);

    if (error) {
      console.error('[PlanService] Error fetching day rewards summary:', error);
      throw error;
    }

    // Group by day_number
    const dayMap = new Map<number, DayRewardsSummary>();

    (data || []).forEach((reward) => {
      const dayNum = reward.day_number!;
      if (!dayMap.has(dayNum)) {
        dayMap.set(dayNum, {
          dayNumber: dayNum,
          isComplete: false,
          hasComment: false,
        });
      }
      const summary = dayMap.get(dayNum)!;
      if (reward.reward_type === REWARD_TYPES.DAY_COMPLETE) {
        summary.isComplete = true;
      }
      if (reward.reward_type === REWARD_TYPES.DAY_COMMENT) {
        summary.hasComment = true;
      }
    });

    return Array.from(dayMap.values()).sort((a, b) => a.dayNumber - b.dayNumber);
  } catch (error) {
    console.error('[PlanService] fetchDayRewardsSummary error:', error);
    return [];
  }
}

