/**
 * Create a new plan session for a user
 */
export async function createPlanSession(
  planId: string,
  userId: string,
  options?: {
    isShared?: boolean;
    sharedName?: string;
    startDay?: number; // Which day to start on (default: 1)
  }
): Promise<PlanSession | null> {
  try {
    const startDay = options?.startDay ?? 1;

    // Calculate started_at so today = startDay
    // If startDay is 5, we set started_at to 4 days ago
    const startedAt = new Date();
    startedAt.setDate(startedAt.getDate() - (startDay - 1));

    const insertData: PlanSessionInsert = {
      plan_id: planId,
      user_id: userId,
      current_day: startDay,
      started_at: startedAt.toISOString(),
      status: 'active',
      is_shared: options?.isShared ?? false,
      shared_name: options?.sharedName ?? null,
      // invite_code is auto-generated by trigger when is_shared=true
    };

    const { data, error } = await supabase
      .from('plan_sessions')
      .insert(insertData)
      .select('*')
      .single();

    if (error) {
      console.error('[PlanService] Error creating plan session:', error);
      throw error;
    }

    return data;
  } catch (error) {
    console.error('[PlanService] createPlanSession error:', error);
    return null;
  }
}

