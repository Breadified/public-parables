/**
 * Delete a plan session (owner only for shared, or owner of personal session)
 */
export async function deleteSession(
  planSessionId: string,
  userId: string
): Promise<{ success: boolean; error?: string }> {
  try {
    console.log('[PlanService] deleteSession called:', { planSessionId, userId });

    // Verify ownership
    const { data: session, error: fetchError } = await supabase
      .from('plan_sessions')
      .select('user_id, is_shared')
      .eq('id', planSessionId)
      .single();

    console.log('[PlanService] Session lookup result:', { session, fetchError });

    if (fetchError || !session) {
      console.error('[PlanService] Session not found:', fetchError);
      return { success: false, error: 'Session not found' };
    }

    if (session.user_id !== userId) {
      console.error('[PlanService] User is not owner:', { sessionOwner: session.user_id, currentUser: userId });
      return { success: false, error: 'Only the owner can delete this session' };
    }

    // Soft delete the session
    const { error } = await supabase
      .from('plan_sessions')
      .update({ status: 'inactive' })
      .eq('id', planSessionId);

    if (error) {
      console.error('[PlanService] deleteSession update error:', error);
      return { success: false, error: error.message || 'Failed to delete session' };
    }

    console.log('[PlanService] Session deleted successfully');

    // If shared, also deactivate all participants
    if (session.is_shared) {
      const { error: partError } = await supabase
        .from('session_participants')
        .update({ status: 'inactive' })
        .eq('plan_session_id', planSessionId);

      if (partError) {
        console.error('[PlanService] Error deactivating participants:', partError);
        // Don't fail the whole operation if this fails
      }
    }

    return { success: true };
  } catch (error) {
    console.error('[PlanService] deleteSession exception:', error);
    return { success: false, error: error instanceof Error ? error.message : 'Failed to delete session' };
  }
}

/**
 * Check if user can leave a session
 * Owner/last admin cannot leave without transferring ownership first
 */
export async function canUserLeaveSession(
  planSessionId: string,
  userId: string
): Promise<{ canLeave: boolean; reason?: string; needsTransfer?: boolean }> {
  try {
    const userRole = await getUserSessionRole(planSessionId, userId);

    if (!userRole) {
      return { canLeave: false, reason: 'Not a participant' };
    }

    // Regular members can always leave
    if (userRole === 'member') {
      return { canLeave: true };
    }

    // Check if there are other admins/owner
    const { data: admins, error } = await supabase
      .from('session_participants')
      .select('user_id, role')
      .eq('plan_session_id', planSessionId)
      .in('role', ['owner', 'admin'])
      .eq('status', 'active');

    if (error) {
      return { canLeave: false, reason: 'Failed to check permissions' };
    }

    const otherAdmins = (admins || []).filter(a => a.user_id !== userId);

    // Owner can leave if there are other admins
    if (userRole === 'owner') {
      if (otherAdmins.length > 0) {
        // There are admins, one will become owner
        return { canLeave: true };
      } else {
        // No other admins, need to transfer
        return {
          canLeave: false,
          reason: 'You must transfer ownership before leaving',
          needsTransfer: true
        };
      }
    }

    // Admin can leave if owner still exists or there are other admins
    if (userRole === 'admin') {
      const hasOwner = otherAdmins.some(a => a.role === 'owner');
      if (hasOwner || otherAdmins.length > 0) {
        return { canLeave: true };
      } else {
        // Last admin, need to assign someone
        return {
          canLeave: false,
          reason: 'You must assign a new admin before leaving',
          needsTransfer: true
        };
      }
    }

    return { canLeave: true };
  } catch (error) {
    console.error('[PlanService] canUserLeaveSession error:', error);
    return { canLeave: false, reason: 'Failed to check permissions' };
  }
}

