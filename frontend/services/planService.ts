/**
 * Award a reward to a user in a session
 * Handles duplicate prevention via upsert
 * @param customPoints - Optional custom points (overrides default for reward type)
 * @param isOnTime - Whether this is an on-time completion (for DAY_COMPLETE rewards)
 */
export async function awardReward(
  planSessionId: string,
  userId: string,
  rewardType: RewardType,
  dayNumber?: number,
  referenceId?: string,
  customPoints?: number,
  isOnTime?: boolean
): Promise<SessionUserReward | null> {
  try {
    const points = customPoints !== undefined ? customPoints : (REWARD_POINTS[rewardType] || 0);

    const insertData: SessionUserRewardInsert = {
      plan_session_id: planSessionId,
      user_id: userId,
      reward_type: rewardType,
      day_number: dayNumber ?? null,
      reference_id: referenceId ?? null,
      points,
      is_on_time: isOnTime ?? null,
    };

    // Use upsert to handle duplicates (unique constraint on session+user+type+day)
    const { data, error } = await supabase
      .from('user_rewards')
      .upsert(insertData, {
        onConflict: 'plan_session_id,user_id,reward_type,day_number',
        ignoreDuplicates: true,
      })
      .select('*')
      .single();

    if (error) {
      // Ignore duplicate key errors (already has reward)
      if (error.code === '23505') {
        console.log('[PlanService] Reward already exists:', rewardType, dayNumber);
        return null;
      }
      console.error('[PlanService] Error awarding reward:', error);
      throw error;
    }

    return data;
  } catch (error) {
    console.error('[PlanService] awardReward error:', error);
    return null;
  }
}

